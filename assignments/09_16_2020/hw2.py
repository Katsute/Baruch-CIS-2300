# get input from user and cast to float
quiz = float(input("quiz: "))
mid_term = float(input("midterm: "))
final = float(input("final: "))

# calc avg
avg = (quiz + mid_term + final) / 3

# if avg > 90 then A
if avg > 90:
    print("Congrats! You got an A")

